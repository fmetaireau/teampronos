package com.betforum;

public class Ligue {
	
	private int id;
	private String libelleLigue;
	private int idSport;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getLibelleLigue() {
		return libelleLigue;
	}
	public void setLibelleLigue(String libelleLigue) {
		this.libelleLigue = libelleLigue;
	}
	public int getIdSport() {
		return idSport;
	}
	public void setIdSport(int idSport) {
		this.idSport = idSport;
	}

}
